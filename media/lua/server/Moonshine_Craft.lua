Recipe = Recipe or {}
Recipe.OnGiveXP = Recipe.OnGiveXP or {}
local mult = 2

function Recipe.OnGiveXP.MoonshineCraftXP(recipe, ingredients, result, player)
    if mult == 1 then return end
    player:getXp():AddXP(Perks.Electricity, 1*mult);
    player:getXp():AddXP(Perks.MetalWelding, 2*mult);
end

function Recipe.OnGiveXP.MoonshineCraftXP2(recipe, ingredients, result, player)
    if mult == 1 then return end
    player:getXp():AddXP(Perks.Electricity, 2*mult);
    player:getXp():AddXP(Perks.MetalWelding, 1*mult);
end

